
package clase;


public class clase 
{
    private double horaxdia;
     private double dia;
     private double pagoxdia;
     private double ingreso;
     private double renta;
     private double neto;

    public clase() {
            
    }

    public clase(double horaxdia, double dia, double pagoxdia) {
        this.horaxdia = horaxdia;
        this.dia = dia;
        this.pagoxdia = pagoxdia;
    }

    public double getHoraxdia() {
        return horaxdia;
    }

    public void setHoraxdia(double horaxdia) {
        this.horaxdia = horaxdia;
    }

    public double getDia() {
        return dia;
    }

    public void setDia(double dia) {
        this.dia = dia;
    }

    public double getPagoxdia() {
        return pagoxdia;
    }

    public void setPagoxdia(double pagoxdia) {
        this.pagoxdia = pagoxdia;
    }

    public double getIngreso() {
        return ingreso;
    }

    public void setIngreso(double ingreso) {
        this.ingreso = ingreso;
    }

    public double getRenta() {
        return renta;
    }

    public void setRenta(double renta) {
        this.renta = renta;
    }

    public double getNeto() {
        return neto;
    }

    public void setNeto(double neto) {
        this.neto = neto;
    }

    
     
     

   
}
